function somaColunas(matriz) {
    let somaColunas = new Array(matriz[0].length).fill(0);

    matriz.forEach(linha => {
        linha.forEach((elemento, indice) => {
            somaColunas[indice] += elemento;
        });
    });

    return somaColunas;
}

